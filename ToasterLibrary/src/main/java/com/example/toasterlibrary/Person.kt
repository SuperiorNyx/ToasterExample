package com.example.toasterlibrary

class Person {
    private var name: String = ""
    private var eye_color: String = ""
    private var url: String = ""

    fun getPersonName() : String {
        return this.name
    }

    fun getEyeColor() : String {
        return this.eye_color
    }

    fun getUrl() : String {
        return this.url
    }

}